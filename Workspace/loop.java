public class loop {
    public static void main(String[] args) {

    // For Loop
    for(int i=0; i<5; i++ )
    {
        System.out.println("Body of loop ");
    }
    
    //While Loop
    int j = 0;
    while(j<5)
    {
        System.out.println("Weeee");
        j++;
    }

    // do-while loop 
    int k=0;
    do 
    {
        System.out.println("Do-while");
        k++;
    } while(k<5);
}
}
